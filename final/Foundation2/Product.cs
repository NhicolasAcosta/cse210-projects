class Product
{
    private string name;
    private int id;
    private decimal price;
    private int quantity;

    public string Name
    {
        get { return name; }
        set { name = value; }
    }

    public int Id
    {
        get{return id;}
        set{id = value;}
    }

    public decimal Price
    {
        get{return price;}
        set{price = value;}
    }

    public int Quantity
    {
        get{return quantity;}
        set{quantity = value;}
    }
}

